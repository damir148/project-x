<script>
	$(function() {
		$('.no-hours-btn').click(function(){
			$('.hide-me').slideToggle("slow");
		});
		
		$('.hours-btn').click(function(){
			$('.hide-me').show("slow");
			
			var start = $(this).attr('data-start');
			var end = $(this).attr('data-end');
			$(".slider-range").slider({values: [start, end]});
			
			switch (end) {
				case '1020':
					$(".slider-time").html("9:00 AM");
					$(".slider-time2").html("5:00 PM");
					break;
				case '1440':
					$(".slider-time").html("12:00 AM");
					$(".slider-time2").html("11:59 PM");
					break;
				default:
			}
			
		});
		
		
		$( ".closed-open" ).click(function() {
			var start = $(this).attr('data-start');
			var end = $(this).attr('data-end');
			
		  $(this).parent().siblings('.time-range').find('.slider-range').slider({values:[start,end]});
		  
		  switch (end) {
		  case '1440':
			  $(this).parent().siblings('.display-times').find('.slider-time').html("12:00 AM");
			  $(this).parent().siblings('.display-times').find('.slider-time2').html("11:59 PM");
		  	break;
		  default:
			  $(this).parent().siblings('.display-times').find('.slider-time').html("0:00 AM");
			  $(this).parent().siblings('.display-times').find('.slider-time2').html("0:00 AM");
		  }
		  
		});
		
		
		$(".slider-range").slider({
		    range: true,
		    min: 0,
		    max: 1440,
		    step: 15,
		    values: [540, 1020],
		    slide: function (e, ui) {				
				console.log(ui.values[1]);
				
		        var hours1 = Math.floor(ui.values[0] / 60);
		        var minutes1 = ui.values[0] - (hours1 * 60);

		        if (hours1.length == 1) hours1 = '0' + hours1;
		        if (minutes1.length == 1) minutes1 = '0' + minutes1;
		        if (minutes1 == 0) minutes1 = '00';
		        if (hours1 >= 12) {
		            if (hours1 == 12) {
		                hours1 = hours1;
		                minutes1 = minutes1 + " PM";
		            } else {
		                hours1 = hours1 - 12;
		                minutes1 = minutes1 + " PM";
		            }
		        } else {
		            hours1 = hours1;
		            minutes1 = minutes1 + " AM";
		        }
		        if (hours1 == 0) {
		            hours1 = 12;
		            minutes1 = minutes1;
		        }



		        $(this).parent().parent().parent().find('.display-times .slider-time').html(hours1 + ':' + minutes1);
				
		        var hours2 = Math.floor(ui.values[1] / 60);
		        var minutes2 = ui.values[1] - (hours2 * 60);

		        if (hours2.length == 1) hours2 = '0' + hours2;
		        if (minutes2.length == 1) minutes2 = '0' + minutes2;
		        if (minutes2 == 0) minutes2 = '00';
		        if (hours2 >= 12) {
		            if (hours2 == 12) {
		                hours2 = hours2;
		                minutes2 = minutes2 + " PM";
		            } else if (hours2 == 24) {
		                hours2 = 11;
		                minutes2 = "59 PM";
		            } else {
		                hours2 = hours2 - 12;
		                minutes2 = minutes2 + " PM";
		            }
		        } else {
		            hours2 = hours2;
		            minutes2 = minutes2 + " AM";
		        }
				
		        $(this).parent().parent().parent().find('.display-times .slider-time2').html(hours2 + ':' + minutes2);
		    }
		});
	});
</script>